<template>
    <div class="infinite-list-wrapper" style="height: 600px;overflow-y: auto;overflow:auto">
        <ul
                class="list"
                v-infinite-scroll="load"
                infinite-scroll-disabled="disabled">
            <li v-for="i in count" class="list-item">{{ i }}</li>
        </ul>
        <p v-if="loading">加载中...</p>
        <p v-if="noMore">没有更多了</p>
    </div>
</template>

<script>
    export default {
        name: "InfiniteScroll",
        data () {
            return {
                count: 10,
                loading: false
            }
        },
        computed: {
            noMore () {
                return this.count >= 60
            },
            disabled () {
                return this.loading || this.noMore
            }
        },
        methods: {
            load () {
                this.loading = true
                setTimeout(() => {
                    this.count += 2
                    this.loading = false
                }, 1000)
            }
        }
    }
</script>

